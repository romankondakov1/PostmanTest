// завдання 4.3

sayHi();        // це працює, тому що функція sayHi оголошена за допомогою function declaration, 
                // яка піднімається (hoisted) вгору свого контексту. Тому виклик sayHi() до оголошення функції працює без проблем.
function sayHi() {
  console.log("Привіт!");
}

sayBye();       // тут беде помилка, тому що sayBye оголошена за допомогою function expression, яка не піднімається (hoisted) вгору свого контексту. спочатку виконується код, і коли досягає виклику sayBye(), змінна sayBye ще не визначена, тому виникає помилка ReferenceError.
const sayBye = function () {
  console.log("Бувай!");
};