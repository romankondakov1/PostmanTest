// завдання 3.3
// грубо кажучі, var по ієрархії дивиться ширше ніж let і const. var має функціональну область видимості, тому вона доступна в межах всієї функції, в якій вона оголошена, а також поза нею. let і const мають блокову область видимості, тому вони доступні лише в межах блоку, в якому вони оголошені (наприклад, всередині if, for, while тощо). Це означає, що var може бути доступним у більш широкому контексті, ніж let і const.

