console.log(first);  
var first = "var-змінна"; //// виведе undefined, тому що змінна first оголошена за допомогою var, і вона піднімається (hoisted) вгору свого контексту. Однак, її значення ще не присвоєно на момент виклику console.log, тому вона має значення undefined.

console.log(second); 
let second = "let-змінна"; //// виведе помилку ReferenceError, тому що змінна second оголошена за допомогою let, тому виклик console.log(second) до оголошення змінної second призведе до помилки.
